import { createLuxuryItemWithUpgrades } from "./luxuryUpgrades";

const createLuxuryItem = (data: {
  id: string;
  name: string;
  icon: string;
  cost: number;
  multiplierBonus: number;
  category: 'vehicle' | 'jewelry' | 'art' | 'collectible';
  description: string;
  prestigeRequirement?: number;
  brandScore?: number;
  imageUrl?: string;
}): LuxuryItem => {
  return {
    ...createLuxuryItemWithUpgrades({
      ...data,
      owned: false,
      upgradePolish: 0,
      upgradeRefit: 0,
      upgradeEntourage: false,
      prestigeRequirement: data.prestigeRequirement ?? 0,
      brandScore: data.brandScore ?? 1,
    }),
  };
};

